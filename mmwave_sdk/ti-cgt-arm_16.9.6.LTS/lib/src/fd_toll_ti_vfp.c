/*--------------------------------------------------------------------------*/
/* Wrapper function for __aeabi_d2lz.                                       */
/*                                                                          */
/* The EABI standard requires floating-point helper functions should pass   */
/* and return variables using the ARM core registers. Prior to release 4.6  */
/* these functions in the RTS used VFP registers for passing and returning  */
/* variables. This function is to be used by files compiled with an older   */
/* version of the compiler. The linker is responsible for remapping the     */
/* symbol.                                                                  */
/*--------------------------------------------------------------------------*/
long long __aeabi_d2lz_TI_VFP(double x)
{
    return (long long)x;
}
