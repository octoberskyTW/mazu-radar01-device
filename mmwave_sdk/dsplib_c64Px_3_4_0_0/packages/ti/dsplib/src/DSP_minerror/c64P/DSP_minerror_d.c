int main()
{
    clock_t t_overhead, t_start, t_stop;
    clock_t t_c, t_i;
    int fail = 0;
    int form_error = 0;

    /* =================================================================== */
    /* Initialize timer for clock */
    TSCL= 0,TSCH=0;
    /* Compute overhead of calling _itoll(TSCH, TSCL) twice to get timing info        */
    /* =================================================================== */
    t_start = _itoll(TSCH, TSCL);
    t_stop = _itoll(TSCH, TSCL);
    t_overhead = t_stop - t_start;

    /* =================================================================== */
    /* Call the individual timing routines, and subtract off overhead      */
    /* =================================================================== */
    t_c = time_c() - t_overhead;    // Calculate time to run Natural C Algorithm
    t_i = time_i() - t_overhead;    // Calculate time to run Optimized C Algorithm

    /* =================================================================== */
    /* Print timing results                                                */
    /* =================================================================== */
    printf("DSP_minerror\t");

    /* =================================================================== */
    /* Check the results arrays, and report any failures                   */
    /* =================================================================== */
    if (memcmp(&max_index_i, &max_index_c, sizeof(max_index_c))) {
        fail++;
        printf("Result Failure (max_index_i)\t");
    }
    else
        printf("Result Successful (max_index_i)\t");
    if (memcmp(&ret_value_i, &ret_value_c, sizeof(ret_value_c))) {
        fail++;
        printf("Result Failure (ret_value_i)");
    }
    else
        printf("Result Successful (ret_value_i)");

    printf("\tnatC: %d\toptC: %d\n", t_c, t_i);

    /* Provide memory information */
#ifdef __TI_COMPILER_VERSION__            // for TI compiler only
    printf("Memory:  %d bytes\n", &kernel_size);
#endif

    /* Provide profiling information */
    printf("Cycles:  %d\n", t_i);

    return(fail);
}

